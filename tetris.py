def main():
    global grid
    pygame.mixer.music.play(-1)
    locked_positions = {}  
    grid = create_grid(locked_positions)
 
    change_piece = False
    run = True
    current_piece = get_shape()
    next_piece = get_shape()
    clock = pygame.time.Clock()
    fall_time = 0
 
    start_timer = True  
    countdown_start_time = None
    countdown_duration = 3 

    while start_timer:
        win.fill((0, 0, 0))
        draw_text_middle("Game starting in...", 40, (255, 0, 0), win)

        if countdown_start_time is None:
            countdown_start_time = time.time()

        current_time = time.time()
        elapsed_time = current_time - countdown_start_time
        remaining_time = countdown_duration - int(elapsed_time)
        if remaining_time <= 0:
            start_timer = False
            run = True
        font = pygame.font.SysFont('comicsans', 60, bold=True)
        label = font.render(str(remaining_time), 1, (0, 255, 0))
        win.blit(label, (top_left_x + play_width/2 - (label.get_width() / 2), top_left_y + play_height/1.5 - label.get_height()/2))
        pygame.display.update()

    while run:  
        fall_speed = 0.3
        grid = create_grid(locked_positions)
        fall_time += clock.get_rawtime()
        clock.tick()
 
        if fall_time/1000 >= fall_speed:
            fall_time = 0
            current_piece.y += 1
            if not (valid_space(current_piece, grid)) and current_piece.y > 0:
                current_piece.y -= 1
                change_piece = True
 
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False
                pygame.display.quit()
                quit()
            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_LEFT:
                    current_piece.x -= 1
                    if not valid_space(current_piece, grid):
                        current_piece.x += 1
                elif event.key == pygame.K_RIGHT:
                    current_piece.x += 1
                    if not valid_space(current_piece, grid):
                        current_piece.x -= 1
                elif event.key == pygame.K_UP:
                    current_piece.rotation = current_piece.rotation + 1 % len(current_piece.shape)
                    if not valid_space(current_piece, grid):
                        current_piece.rotation = current_piece.rotation - 1 % len(current_piece.shape)
                if event.key == pygame.K_DOWN:
                    current_piece.y += 1
                    if not valid_space(current_piece, grid):
                        current_piece.y -= 1
                if event.key == pygame.K_ESCAPE:
                    run = False
                    pygame.display.quit()
                    quit()

        shape_pos = convert_shape_format(current_piece)
 
        for i in range(len(shape_pos)):
            x, y = shape_pos[i]
            if y > -1:
                grid[y][x] = current_piece.color
 
        if change_piece:
            for pos in shape_pos:
                p = (pos[0], pos[1])
                locked_positions[p] = current_piece.color
            current_piece = next_piece
            next_piece = get_shape()
            change_piece = False
            clear_rows(grid, locked_positions)
 
        draw_window(win)
        draw_next_shape(next_piece, win)
        pygame.display.update()
 
        if check_lost(locked_positions):
            run = False
 
    draw_text_middle("You Lost", 40, (255,255,255), win)
    pygame.display.update()
    pygame.time.delay(2000)
 
 
